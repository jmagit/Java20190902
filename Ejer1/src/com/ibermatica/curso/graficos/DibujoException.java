package com.ibermatica.curso.graficos;

public class DibujoException extends Exception {
	private static final long serialVersionUID = 1L;

	public DibujoException(String arg0) {
		super(arg0);
	}

	public DibujoException(Throwable arg0) {
		super(arg0);
	}

	public DibujoException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
