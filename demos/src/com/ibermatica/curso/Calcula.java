package com.ibermatica.curso;

public interface Calcula {
	static double suma(double o1, double o2) { return o1 + o2; }
	static double resta(double o1, double o2) { return o1 - o2; }
	static double multiplica(double o1, double o2) { return o1 * o2; }
	static double divide(double o1, double o2) { return o1 / o2; }
	
}
